What is it?

First Aid Angel: Emergency Response CPD Refresher is a short online professional development course covering common first aid emergencies and immediate response steps.

The course helps learners review key actions such as:

- recognising an emergency
- following DRSABCD
- calling Triple Zero (000)
- checking airway and breathing
- understanding CPR and AED basics
- responding to choking, bleeding, burns, asthma, anaphylaxis, stroke, heart attack, seizures, shock, hypoglycaemia, drowning, head injury, and electric shock
- knowing when to escalate to emergency services
- understanding what not to do in critical situations

Learners complete online lessons and knowledge checks, then receive a Certificate of Completion showing CPD hours recorded.

This course is designed to support confidence, recall, and emergency readiness — not to assess practical hands-on skills.

