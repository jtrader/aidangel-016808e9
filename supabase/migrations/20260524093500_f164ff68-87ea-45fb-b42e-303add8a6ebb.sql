## D — Defibrillation: Use an AED

An AED (Automated External Defibrillator) can help restart a heart.

:::steps[Using an AED]
1. Ask someone to find an AED if one is nearby.
2. Turn it on and listen to the voice prompts.
3. Stick the pads onto the person as shown in the pictures on the AED.
4. Make sure no one touches the person while the AED is working or giving a shock.
5. Get straight back to CPR after any shock.
:::

