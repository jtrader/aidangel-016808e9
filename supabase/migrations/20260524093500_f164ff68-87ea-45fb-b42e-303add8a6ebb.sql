## C — CPR: Keep the Blood Flowing

If the person isn''t breathing normally, start CPR.

:::steps[How to do CPR]
1. Place the heel of one hand in the centre of their chest.
2. Put your other hand on top and link your fingers.
3. Push hard and fast: 100 to 120 pushes per minute.
4. Push down about one-third of their chest depth (about 5-6 cm for adults).
5. Let their chest come all the way up between pushes.
6. If you''re trained and happy to, give 2 rescue breaths after every 30 pushes.
7. If you can''t or won''t give breaths, just do chest pushes (hands-only CPR).
:::

