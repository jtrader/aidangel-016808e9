Imagine a tiny, shiny disc – like a coin, but smaller. That''s a button battery. They''re in toys, remote controls, watches, and even some hearing aids. For little ones, these can look like enticing lollies or something fun to play with. But if swallowed, they become a silent, dangerous threat.

