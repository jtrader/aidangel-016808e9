### Why are button batteries so dangerous?

It might not seem obvious, but once a button battery gets wet inside the body (like with saliva), it starts to create an electrical current. This current can cause a chemical reaction that burns through surrounding tissue, like the oesophagus (food pipe), incredibly quickly. We''re talking hours, not days, before serious damage occurs.

:::remember[Time is Tissue!]
The faster a swallowed button battery is removed, the less damage it can do. Every minute counts!
:::

### What to do if a button battery is swallowed

:::steps[Immediate Actions]
1. **Call [000](tel:000) straight away.** Tell them it''s a suspected button battery ingestion.
2. **Do NOT try to make the person vomit.** This can cause more harm.
3. **Do NOT give them anything to eat or drink** unless specifically told to do so by medical professionals.
4. **Take the person to the nearest emergency department.** If you know, tell them the type of battery (e.g., from a remote control).
:::

